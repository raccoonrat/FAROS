from typing import Any, Dict

from app.faros.capabilities.base import BaseCapability
from app.faros.models.artifact import ArtifactRecord
from app.faros.models.capability import CapabilityResult
from app.faros.models.execution import ExecutionContext
from app.modules.code.projects import code_project_service
from app.modules.code.storage import get_session_context
from app.modules.platform.storage import create_experiment


class ExperimentCapability(BaseCapability):
    capability_id = "experiment"
    description = "Create an LLM-domain experiment workspace by provisioning a code project and an experiment record."

    def execute(self, context: ExecutionContext, inputs: Dict[str, Any]) -> CapabilityResult:
        selected_candidate = inputs.get("selectedCandidate") or {}
        session_id = inputs.get("ideaSessionId")
        candidate_id = inputs.get("selectedCandidateId")
        paper_type = inputs.get("paperType", "algorithm")
        title = selected_candidate.get("title") or inputs.get("seedQuery") or "FAROS Experiment"
        description = selected_candidate.get("problem") or inputs.get("notes") or "Experiment workspace generated by FAROS."
        language = inputs.get("language", "python")
        framework = inputs.get("framework", "FastAPI")

        with get_session_context() as db:
            project = code_project_service.create_project(
                db=db,
                title=f"{title} [{language}]",
                description=description,
                language=language,
                framework=framework,
                source_idea_session_id=session_id,
                source_candidate_id=candidate_id,
            )

            readme = f"# {title}\n\n"
            readme += "Generated by FAROS experiment capability for the LLM workflow.\n\n"
            if selected_candidate:
                readme += f"## Problem\n{selected_candidate.get('problem', '')}\n\n"
                readme += f"## Key Insight\n{selected_candidate.get('keyInsight', '')}\n\n"
            readme += "## Structure\n- `src/` runtime code\n- `configs/` experiment configuration\n- `scripts/` execution entrypoints\n- `tests/` basic smoke tests\n"

            config_json = {
                "title": title,
                "paperType": paper_type,
                "framework": framework,
                "seedQuery": inputs.get("seedQuery"),
                "selectedCandidateId": candidate_id,
                "ideaSessionId": session_id,
            }

            files = [
                {"path": "README.md", "content": readme},
                {"path": "requirements.txt", "content": "fastapi\nuvicorn\npydantic\n"},
                {"path": "configs/experiment.json", "content": __import__('json').dumps(config_json, indent=2)},
                {"path": "src/main.py", "content": "def main():\n    print(\"FAROS experiment scaffold\")\n\n\nif __name__ == '__main__':\n    main()\n"},
                {"path": "src/pipeline.py", "content": "def build_experiment_plan():\n    return {\"status\": \"draft\"}\n"},
                {"path": "scripts/run.sh", "content": "#!/usr/bin/env bash\npython src/main.py\n"},
                {"path": "tests/test_smoke.py", "content": "from src.main import main\n\n\ndef test_main_runs():\n    assert main() is None\n"},
            ]
            code_project_service.write_project_files(db, project.id, files)

        experiment = create_experiment({
            "name": f"{title} Experiment",
            "projectId": project.id,
            "description": description,
            "tags": ["faros", "llm", paper_type],
            "status": "designed",
        })

        return CapabilityResult(
            status="completed",
            outputs={
                "projectId": project.id,
                "projectTitle": project.title,
                "experimentId": experiment["id"],
                "experimentStatus": experiment.get("status"),
                "experimentIds": [experiment["id"]],
            },
            artifacts=[
                ArtifactRecord(
                    id=f"{context.run_id}:{self.capability_id}:project",
                    type="code_project",
                    uri=f"project://{project.id}",
                    producer=self.capability_id,
                    summary=f"Project {project.id} created for experiment stage",
                    metadata={"projectId": project.id, "language": language, "framework": framework},
                ),
                ArtifactRecord(
                    id=f"{context.run_id}:{self.capability_id}:experiment",
                    type="experiment_record",
                    uri=f"experiment://{experiment['id']}",
                    producer=self.capability_id,
                    summary=f"Experiment {experiment['id']} linked to project {project.id}",
                    metadata={"experimentId": experiment['id'], "projectId": project.id},
                ),
            ],
            events=[
                {"level": "info", "message": f"Experiment stage provisioned project {project.id} and experiment {experiment['id']}"}
            ],
        )
